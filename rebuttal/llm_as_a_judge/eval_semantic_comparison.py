def _build_scdd_checkpoint_loader():
    import tokenizers as _tk

    SCRIPT_DIR = Path(__file__).parent.resolve()
    sys.path.insert(0, str(SCRIPT_DIR.parent / "mdlm_v2"))
    sys.path.insert(0, str(SCRIPT_DIR.parent / "mdlm_v2" / "mdlm"))
    import dataloader  # noqa: E402
    import diffusion   # noqa: E402

    def _load(checkpoint_path, device, nucleus_p):
        orig_setstate = _tk.Tokenizer.__setstate__
        try:
            _tk.Tokenizer.__setstate__ = lambda self, state: None
            checkpoint = torch.load(
                checkpoint_path, map_location=device, weights_only=False)
        finally:
            _tk.Tokenizer.__setstate__ = orig_setstate

        config = checkpoint["hyper_parameters"]["config"]
        tokenizer = dataloader.get_tokenizer(config)
        model = diffusion.Diffusion(config, tokenizer=tokenizer)
        model.load_state_dict(checkpoint["state_dict"], strict=False)
        if model.ema is not None and "ema" in checkpoint:
            model.ema.load_state_dict(checkpoint["ema"])
        model = model.to(device)
        model.config.sampling.nucleus_p = nucleus_p
        return model

    return _load


def _preflight_scdd_setup(model, clean_ids):
    tokenizer = model.tokenizer
    if tokenizer.pad_token_id is None:
        raise ValueError("SCDD tokenizer must define a pad token.")
    if model.mask_index != tokenizer.pad_token_id:
        raise ValueError(
            f"Expected mask_index == pad_token_id, got {model.mask_index} vs "
            f"{tokenizer.pad_token_id}.")
    if clean_ids.min().item() < 0 or clean_ids.max().item() >= tokenizer.vocab_size:
        raise ValueError("Clean ids out of base vocabulary range.")


def _build_tokenizer_metadata(model):
    tokenizer = model.tokenizer
    return {
        "base_vocab_size": tokenizer.vocab_size,
        "pad_token_id": tokenizer.pad_token_id,
        "mask_token_id": tokenizer.mask_token_id,
        "mask_index": model.mask_index,
    }


def _sample_uniform_base_tokens(clean_ids, base_vocab_size):
    draws = torch.randint(
        0, base_vocab_size - 1, clean_ids.shape, device=clean_ids.device)
    return draws + (draws >= clean_ids).to(draws.dtype)


def _materialize_corruption(clean_ids, mask_positions, uniform_positions,
                            uniform_tokens, mask_token_id):
    out = clean_ids.clone()
    out[mask_positions] = mask_token_id
    out[uniform_positions] = uniform_tokens[uniform_positions]
    return out


def _decode_corrupted_display(tokenizer, visible_ids, mask_positions):
    texts = []
    for seq_ids, seq_mask in zip(visible_ids.tolist(), mask_positions.tolist()):
        pieces, buffer = [], []
        for token_id, is_mask in zip(seq_ids, seq_mask):
            if is_mask:
                if buffer:
                    pieces.append(tokenizer.decode(buffer, skip_special_tokens=False,
                                                   clean_up_tokenization_spaces=False))
                    buffer = []
                pieces.append("[MASK]")
            else:
                buffer.append(token_id)
        if buffer:
            pieces.append(tokenizer.decode(buffer, skip_special_tokens=False,
                                           clean_up_tokenization_spaces=False))
        texts.append("".join(pieces))
    return texts


def _build_corruptions_shared(scdd, clean_ids, t_start=T_START_DEFAULT):
    """Corrupt clean_ids using SCDD's forward process at time t_start.

    Both SCDD and GIDD+ receive the same corrupted input (with model-specific
    mask token IDs), ensuring a fair paired comparison.
    """
    batch = clean_ids.shape[0]
    base_vocab_size = scdd.tokenizer.vocab_size
    gidd_mask_id = base_vocab_size + 1   # GIDD+ uses vocab_size + 1 as mask

    torch.manual_seed(SEED)
    t = torch.full((batch,), t_start, device=scdd.device)
    alpha_bar, beta_bar = scdd._get_alpha_beta_bar(
        t, scdd.config.forward.ratio,
        scdd.config.forward.gamma, scdd.config.forward.t_peak)
    mask_chance = (1 - alpha_bar - beta_bar)[:, None]
    uniform_chance = (
        (scdd.vocab_size - 2) * beta_bar / (scdd.vocab_size - 1))[:, None]

    draws = torch.rand(clean_ids.shape, device=clean_ids.device)
    mask_positions = draws < mask_chance
    uniform_positions = (draws >= mask_chance) & (draws < mask_chance + uniform_chance)
    uniform_tokens = _sample_uniform_base_tokens(clean_ids, base_vocab_size)

    corrupted_visible = clean_ids.clone()
    corrupted_visible[uniform_positions] = uniform_tokens[uniform_positions]
    corrupted_scdd = _materialize_corruption(
        clean_ids, mask_positions, uniform_positions, uniform_tokens, scdd.mask_index)
    corrupted_gidd = _materialize_corruption(
        clean_ids, mask_positions, uniform_positions, uniform_tokens, gidd_mask_id)
    return {
        "scdd": corrupted_scdd,
        "gidd": corrupted_gidd,
        "display": _decode_corrupted_display(
            scdd.tokenizer, corrupted_visible, mask_positions),
        "gidd_expected": {
            "base_vocab_size": base_vocab_size,
            "pad_token_id": base_vocab_size,
            "mask_token_id": gidd_mask_id,
            "len_tokenizer": base_vocab_size + 2,
        },
    }


def _load_owt_texts(tokenizer, device):
    from datasets import load_dataset
    rng = random.Random(SEED)
    ds = load_dataset("openwebtext", split="train",
                      streaming=True, trust_remote_code=True)
    pool = []
    for item in ds:
        ids = tokenizer.encode(item["text"], add_special_tokens=False)
        if len(ids) >= SEQ_LEN:
            start = rng.randint(0, len(ids) - SEQ_LEN)
            pool.append(ids[start : start + SEQ_LEN])
        if len(pool) >= NUM_TEXTS * 5:
            break
    rng.shuffle(pool)
    return torch.tensor(pool[:NUM_TEXTS], dtype=torch.long, device=device)


# ═══════════════════════════════════════════════════════════════════════════════
# SCDD generation
# ═══════════════════════════════════════════════════════════════════════════════

@torch.no_grad()
def _generate_scdd(model, x_t_all, correction_steps, t_start, batch_size, device):
    import itertools

    if model.ema:
        model.ema.store(itertools.chain(
            model.backbone.parameters(), model.noise.parameters()))
        model.ema.copy_to(itertools.chain(
            model.backbone.parameters(), model.noise.parameters()))
    model.backbone.eval()
    model.noise.eval()

    eps = T_EPS_SCDD
    timesteps = torch.linspace(t_start, eps, correction_steps + 1, device=device)
    dt = (t_start - eps) / correction_steps

    all_out = []
    try:
        for start in range(0, x_t_all.shape[0], batch_size):
            x = x_t_all[start : start + batch_size].clone()
            for i in range(correction_steps):
                t = timesteps[i] * torch.ones(x.shape[0], 1, device=device)
                x = model._scdlm_update(x, t, dt)
            t_final = timesteps[-1] * torch.ones(x.shape[0], 1, device=device)
            unet_cond = model.noise(t_final)[0]
            x = model.forward(x, unet_cond).argmax(dim=-1)
            all_out.append(x)
    finally:
        if model.ema:
            model.ema.restore(itertools.chain(
                model.backbone.parameters(), model.noise.parameters()))

    out = torch.cat(all_out, dim=0)
    return [model.tokenizer.decode(out[i].tolist(), skip_special_tokens=True)
            for i in range(out.shape[0])]


def run_scdd(args):
    rank       = args.rank
    world_size = args.world_size
    s_idx, e_idx = _rank_slice(rank, world_size)

    work_dir = Path(args.work_dir)
    work_dir.mkdir(parents=True, exist_ok=True)
    set_seeds()
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

    print(f"[rank {rank}] Loading SCDD...")
    load_scdd = _build_scdd_checkpoint_loader()
    scdd = load_scdd(args.scdd_ckpt, device, args.nucleus_p)
    tokenizer = scdd.tokenizer

    if rank == 0:
        print("Loading OWT texts...")
        clean_ids = _load_owt_texts(tokenizer, device)
        _preflight_scdd_setup(scdd, clean_ids)

        t_start_val = args.t_start
        print(f"Corrupting (shared corruption at t={t_start_val})...")
        corruption = _build_corruptions_shared(scdd, clean_ids, t_start=t_start_val)

        torch.save(corruption["scdd"].cpu(), work_dir / "corrupted_scdd.pt")
        torch.save(corruption["gidd"].cpu(), work_dir / "corrupted_gidd.pt")
        clean_decoded = [tokenizer.decode(clean_ids[i].tolist(), skip_special_tokens=True)
                         for i in range(NUM_TEXTS)]
        with open(work_dir / "clean_decoded.json", "w") as f:
            json.dump(clean_decoded, f, indent=2)
        with open(work_dir / "corrupted_decoded.json", "w") as f:
            json.dump(corruption["display"], f, indent=2)
        save_t_starts(args.work_dir, {"scdd": t_start_val, "gidd": t_start_val})
        update_work_metadata(
            args.work_dir,
            corruption_mode="shared",
            seed=SEED, num_texts=NUM_TEXTS, seq_len=SEQ_LEN,
            nucleus_p=args.nucleus_p,
            scdd_ckpt=args.scdd_ckpt,
            scdd_tokenizer=_build_tokenizer_metadata(scdd),
            gidd_expected=corruption["gidd_expected"],
            forward_ratio=scdd.config.forward.ratio,
            forward_gamma=scdd.config.forward.gamma,
            forward_t_peak=scdd.config.forward.t_peak,
        )
        print("[rank 0] Corruption files saved.")
    else:
        t_starts_path = Path(args.work_dir) / "t_starts.json"
        print(f"[rank {rank}] Waiting for rank 0 to finish corruption setup...")
        while not t_starts_path.exists():
            time.sleep(3)
        print(f"[rank {rank}] Corruption files ready.")

    corrupted_scdd = torch.load(
        work_dir / "corrupted_scdd.pt", weights_only=True).to(device)
    t_start_scdd = load_t_starts(args.work_dir)["scdd"]
    corrupted_slice = corrupted_scdd[s_idx:e_idx]

    for total_steps in TOTAL_STEPS_LIST:
        correction_steps = total_steps // 2
        out_path = _shard_path(
            work_dir / f"scdd_texts_steps{total_steps}.json", rank, world_size)
        if out_path.exists():
            print(f"  [rank {rank} steps={total_steps}] Exists, skipping.")
            continue
        print(f"  [rank {rank} steps={total_steps}] Generating "
              f"samples {s_idx}-{e_idx-1} ({correction_steps} correction steps)...")
        texts = _generate_scdd(
            scdd, corrupted_slice, correction_steps, t_start_scdd,
            args.batch_size, device)
        with open(out_path, "w") as f:
            json.dump(texts, f, indent=2)
        print(f"    Saved: {out_path}")

    print(f"[rank {rank}] Stage scdd complete.")


# ═══════════════════════════════════════════════════════════════════════════════
# GIDD+ generation
# ═══════════════════════════════════════════════════════════════════════════════

def _generate_gidd(sampler, z_t_all, correction_steps, t_start, device, batch_size):
    eps = T_EPS_GIDD
    ts = torch.linspace(t_start, eps, correction_steps + 1,
                        device=device).unsqueeze(-1)
    all_out = []
    with torch.no_grad():
        for start in range(0, z_t_all.shape[0], batch_size):
            z = z_t_all[start : start + batch_size].clone()
            for i in range(correction_steps):
                z = sampler.sampling_step(z, ts[i], ts[i + 1]).clone()
            all_out.append(z)
    out = torch.cat(all_out, dim=0)
    return [sampler.tokenizer.decode(out[i].tolist(), skip_special_tokens=True)
            for i in range(out.shape[0])]


def _preflight_gidd_setup(gidd_tok, gidd_cfg, noise_schedule, metadata):
    if metadata.get("corruption_mode") != "shared":
        raise ValueError(
            f"Expected shared corruption mode, got {metadata.get('corruption_mode')}")

    expected = metadata.get("gidd_expected", {})
    if expected:
        observed = {
            "base_vocab_size": gidd_tok.vocab_size,
            "len_tokenizer": len(gidd_tok),
            "pad_token_id": gidd_tok.pad_token_id,
            "mask_token_id": gidd_tok.mask_token_id,
        }
        mismatches = [f"{k}={observed[k]} (expected {expected[k]})"
                      for k in expected if observed.get(k) != expected[k]]
        if mismatches:
            raise ValueError(
                "GIDD+ tokenizer incompatible with corruption artifacts: "
                + ", ".join(mismatches))

    if gidd_cfg.model.max_seq_len != SEQ_LEN:
        raise ValueError(
            f"GIDD+ max_seq_len={gidd_cfg.model.max_seq_len}, expected {SEQ_LEN}.")

    expected_ratio = metadata.get("forward_ratio")
    if (expected_ratio is not None
            and abs(gidd_cfg.model.p_uniform - expected_ratio) > 1e-8):
        print(f"WARNING: Forward ratio mismatch: SCDD used {expected_ratio} "
              f"but GIDD+ has p_uniform={gidd_cfg.model.p_uniform}. "
              "GIDD+ will denoise from a distribution it was not trained on.")


def run_gidd(args):
    SCRIPT_DIR = Path(__file__).parent.resolve()
    sys.path.insert(0, str(SCRIPT_DIR.parent / "gidd"))
    from gidd.checkpoints import load_checkpoint as load_gidd  # noqa: E402
    from gidd.sampling import get_sampler                      # noqa: E402

    work_dir = Path(args.work_dir)
    set_seeds()
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    gidd_ckpt = _normalize_gidd_ckpt_path(args.gidd_ckpt)

    print("Loading GIDD+...")
    gidd_model, noise_schedule, gidd_tok, gidd_cfg = load_gidd(
        gidd_ckpt, device=device)
    gidd_sampler = get_sampler(
        gidd_cfg, gidd_model, gidd_tok, noise_schedule,
        compile_step=False, nucleus_p=args.nucleus_p)

    rank       = args.rank
    world_size = args.world_size
    if rank == 0:
        metadata = update_work_metadata(
            args.work_dir,
            gidd_ckpt=str(gidd_ckpt),
            gidd_p_uniform=gidd_cfg.model.p_uniform,
        )
    else:
        metadata = load_work_metadata(args.work_dir)
    _preflight_gidd_setup(gidd_tok, gidd_cfg, noise_schedule, metadata)
    s_idx, e_idx = _rank_slice(rank, world_size)

    corrupted_path = work_dir / "corrupted_gidd.pt"
    if not corrupted_path.exists():
        raise FileNotFoundError(f"Missing: {corrupted_path}")
    corrupted_all = torch.load(corrupted_path, map_location=device, weights_only=True)
    corrupted = corrupted_all[s_idx:e_idx]

    t_starts = load_t_starts(args.work_dir)
    t_start_gidd = t_starts["gidd"]

    for total_steps in TOTAL_STEPS_LIST:
        correction_steps = total_steps // 2
        out_path = _shard_path(
            work_dir / f"gidd_texts_steps{total_steps}.json", rank, world_size)
        if out_path.exists():
            print(f"  [rank {rank} steps={total_steps}] Exists, skipping.")
            continue
        print(f"  [rank {rank} steps={total_steps}] Generating "
              f"samples {s_idx}-{e_idx-1} ({correction_steps} correction steps)...")
        texts = _generate_gidd(
            gidd_sampler, corrupted, correction_steps, t_start_gidd,
            device, args.batch_size)
        with open(out_path, "w") as f:
            json.dump(texts, f, indent=2)
        print(f"    Saved: {out_path}")

    print(f"[rank {rank}] Stage gidd complete.")


# ═══════════════════════════════════════════════════════════════════════════════
# Stage: judge
# ═══════════════════════════════════════════════════════════════════════════════

def _judge_pair(client, text_scdd, text_gidd, seed_idx, model_name,
                max_retries=6):
    import openai as _openai

    rng = random.Random(seed_idx)
    flip = rng.random() < 0.5
    ta, tb = (text_gidd, text_scdd) if flip else (text_scdd, text_gidd)

    for attempt in range(max_retries + 1):
        try:
            response = client.chat.completions.create(
                model=model_name,
                messages=[{"role": "user",
                           "content": JUDGE_PROMPT.format(text_a=ta, text_b=tb)}],
                temperature=0.0,
                response_format={"type": "json_object"},
            )
            break
        except (_openai.RateLimitError, _openai.APIError) as e:
            if attempt == max_retries:
                raise
            wait = min(2 ** attempt, 60)
            print(f"    API error ({e}), retrying in {wait}s...")
            time.sleep(wait)

    result = json.loads(response.choices[0].message.content)

    if flip:
        result["A"], result["B"] = result["B"], result["A"]
        for k in result.get("pairwise", {}):
            v = result["pairwise"][k]
            result["pairwise"][k] = (
                "B" if v == "A" else ("A" if v == "B" else "tie"))
        ov = result.get("overall", "tie")
        result["overall"] = (
            "B" if ov == "A" else ("A" if ov == "B" else "tie"))
    return result


def run_judge(args):
    from openai import OpenAI
    from tqdm import tqdm

    rank       = args.rank
    world_size = args.world_size
    s_idx, e_idx = _rank_slice(rank, world_size)

    work_dir = Path(args.work_dir)
    out_dir  = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    set_seeds()

    with open(work_dir / "corrupted_decoded.json") as f:
        corrupted_decoded = json.load(f)

    client = OpenAI(api_key=args.openai_key)

    for total_steps in TOTAL_STEPS_LIST:
        out_path = _shard_path(
            out_dir / f"results_steps{total_steps}.json", rank, world_size)
        if out_path.exists():
            print(f"  [rank {rank} steps={total_steps}] Exists, skipping.")
            continue

        scdd_path = work_dir / f"scdd_texts_steps{total_steps}.json"
        gidd_path = work_dir / f"gidd_texts_steps{total_steps}.json"
        assert scdd_path.exists(), f"Missing: {scdd_path}"
        assert gidd_path.exists(), f"Missing: {gidd_path}"

        with open(scdd_path) as f: scdd_texts = json.load(f)
        with open(gidd_path) as f: gidd_texts = json.load(f)

        # Resume from partial checkpoint
        partial_path = _shard_path(
            out_dir / f"results_steps{total_steps}_partial.json", rank, world_size)
        step_results = []
        start_idx = s_idx
        if partial_path.exists():
            with open(partial_path) as f:
                step_results = json.load(f)
            start_idx = s_idx + len(step_results)

        n_this_rank = e_idx - s_idx
        print(f"\n  [rank {rank} steps={total_steps}] Judging samples "
              f"{s_idx}-{e_idx-1} ({n_this_rank} pairs) with {args.judge_model}...")
        for i in tqdm(range(start_idx, e_idx),
                      initial=start_idx - s_idx, total=n_this_rank):
            judgment = _judge_pair(
                client, scdd_texts[i], gidd_texts[i],
                seed_idx=i, model_name=args.judge_model)
            step_results.append({
                "idx": i,
                "corrupted_text": corrupted_decoded[i] if corrupted_decoded else "",
                "scdd_text": scdd_texts[i],
                "gidd_text": gidd_texts[i],
                "judgment": judgment,
            })
            if len(step_results) % 8 == 0:
                with open(partial_path, "w") as f:
                    json.dump(step_results, f, indent=2)

        with open(out_path, "w") as f:
            json.dump(step_results, f, indent=2)
        if partial_path.exists():
            partial_path.unlink()
        print(f"  Saved: {out_path}")
        _print_summary(step_results, total_steps)

    print(f"[rank {rank}] Stage judge complete.")


