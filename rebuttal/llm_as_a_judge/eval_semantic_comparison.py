# Summary & statistics
# ═══════════════════════════════════════════════════════════════════════════════

def _print_summary(results, total_steps, stats_test=False):
    from scipy import stats as sp_stats

    wins = {"scdd": 0, "gidd": 0, "tie": 0}
    for r in results:
        ov = r["judgment"].get("overall", "tie")
        if ov == "A":   wins["scdd"] += 1
        elif ov == "B": wins["gidd"] += 1
        else:           wins["tie"]  += 1

    n = len(results)
    scdd_scores = {d: [r["judgment"]["A"].get(d, 5) for r in results] for d in DIMS}
    gidd_scores = {d: [r["judgment"]["B"].get(d, 5) for r in results] for d in DIMS}

    print(f"  [steps={total_steps}] Wins — "
          f"SCDD: {wins['scdd']}, GIDD+: {wins['gidd']}, "
          f"Tie: {wins['tie']}  (n={n})")

    if not stats_test:
        print(f"  {'dim':<16} {'SCDD':>6} {'GIDD+':>6}")
        for d in DIMS:
            print(f"  {d:<16} {np.mean(scdd_scores[d]):>6.2f}"
                  f" {np.mean(gidd_scores[d]):>6.2f}")
        return

    print(f"\n  {'dim':<16} {'SCDD':>6} {'GIDD+':>6}  {'diff':>6}  {'SE':>5}"
          f"  {'95% CI':^15}  {'paired-t p':>10}  {'Wilcoxon p':>10}  {'d':>5}")
    print("  " + "-" * 90)

    total_a = np.zeros(n)
    total_b = np.zeros(n)

    for d in DIMS:
        a = np.array(scdd_scores[d], dtype=float)
        b = np.array(gidd_scores[d], dtype=float)
        diffs = a - b
        mean_d = diffs.mean()
        se_d = sp_stats.sem(diffs)
        ci_lo, ci_hi = sp_stats.t.interval(
            0.95, df=len(diffs) - 1, loc=mean_d, scale=se_d)
        t_p = sp_stats.ttest_rel(a, b).pvalue
        try:
            w_p = sp_stats.wilcoxon(diffs).pvalue
        except ValueError:
            w_p = float("nan")
        pooled_std = np.sqrt((a.std(ddof=1) ** 2 + b.std(ddof=1) ** 2) / 2)
        cohens_d = mean_d / pooled_std if pooled_std > 0 else float("nan")

        sig = ("***" if t_p < 0.001 else "**" if t_p < 0.01 else
               "*" if t_p < 0.05 else "")
        print(f"  {d:<16} {a.mean():>6.3f} {b.mean():>6.3f}  {mean_d:>+6.3f}"
              f"  {se_d:>5.3f}  [{ci_lo:>+6.3f},{ci_hi:>+6.3f}]"
              f"  {t_p:>10.4f}{sig:3s}  {w_p:>10.4f}  {cohens_d:>+5.3f}")
        total_a += a
        total_b += b

    # Win-rate binomial test
    n_decisive = wins["scdd"] + wins["gidd"]
    win_rate = wins["scdd"] / n
    win_se = np.sqrt(win_rate * (1 - win_rate) / n)
    binom_p = sp_stats.binomtest(wins["scdd"], n_decisive, 0.5).pvalue \
              if n_decisive > 0 else float("nan")
    sig = ("***" if binom_p < 0.001 else "**" if binom_p < 0.01 else
           "*" if binom_p < 0.05 else "")
    print(f"\n  Win rate (SCDD, excl. ties): {wins['scdd']}/{n_decisive}"
          f" = {wins['scdd']/n_decisive:.3f} +/- {win_se:.3f}"
          f"  binomial p = {binom_p:.4f}{sig}")


