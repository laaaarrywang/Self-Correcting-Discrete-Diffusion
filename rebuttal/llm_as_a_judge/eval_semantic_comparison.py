# Shared helpers
# ═══════════════════════════════════════════════════════════════════════════════

def set_seeds():
    random.seed(SEED)
    np.random.seed(SEED)
    torch.manual_seed(SEED)


def load_t_starts(work_dir):
    path = Path(work_dir) / "t_starts.json"
    if path.exists():
        with open(path) as f:
            return json.load(f)
    return {"scdd": None, "gidd": None}


def save_t_starts(work_dir, t_starts):
    with open(Path(work_dir) / "t_starts.json", "w") as f:
        json.dump(t_starts, f, indent=2)


def load_work_metadata(work_dir):
    path = Path(work_dir) / WORK_METADATA_NAME
    if path.exists():
        with open(path) as f:
            return json.load(f)
    return {}


def update_work_metadata(work_dir, **updates):
    metadata = load_work_metadata(work_dir)
    metadata.update(updates)
    with open(Path(work_dir) / WORK_METADATA_NAME, "w") as f:
        json.dump(metadata, f, indent=2, sort_keys=True)
    return metadata


def _normalize_gidd_ckpt_path(path_str):
    path = Path(path_str)
    if path.is_file() and path.name == "model.pt":
        return path.parent
    return path


def _rank_slice(rank, world_size):
    start = rank * NUM_TEXTS // world_size
    end   = (rank + 1) * NUM_TEXTS // world_size
    return start, end


def _shard_path(path, rank, world_size):
    if world_size == 1:
        return Path(path)
    p = Path(path)
    return p.parent / f"{p.stem}_rank{rank}{p.suffix}"


