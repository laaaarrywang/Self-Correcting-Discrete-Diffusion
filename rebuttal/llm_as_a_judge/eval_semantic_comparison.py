#!/usr/bin/env python3
"""
Semantic comparison: SCDD vs GIDD+ — staged pipeline.

Because SCDD and GIDD+ run in different conda environments, this script is
called four times with --stage {scdd, gidd, judge, merge} from the shell
script, each time under the correct environment.

Intermediate tensors and texts are exchanged via --work_dir on shared storage.

  Stage scdd   (conda: scdd)
    - Loads OWT texts, creates shared corruption using SCDD's forward process,
      generates SCDD outputs for all step counts.

  Stage gidd   (conda: gidd)
    - Loads pre-built corrupted inputs, generates GIDD+ outputs for all step
      counts from the same starting point.

  Stage judge  (needs openai package)
    - Loads all generated texts, calls LLM judge, writes per-step results.

  Stage merge
    - Merges per-rank shards, runs paired statistical tests, prints summary.
"""

import argparse
import json
import os
import random
import sys
import time
from pathlib import Path

import numpy as np
import torch

# ── Constants ─────────────────────────────────────────────────────────────────
TOTAL_STEPS_LIST = [32, 64, 128, 256, 512, 1024]
NUM_TEXTS   = 256
SEQ_LEN     = 512
T_START_DEFAULT = 0.5
T_EPS_SCDD  = 1e-5
T_EPS_GIDD  = 1e-4
SEED        = 42
DIMS        = ["clarity", "grammaticality", "factuality", "style", "creativity"]
WORK_METADATA_NAME = "work_metadata.json"

JUDGE_PROMPT = """\
You will evaluate two text excerpts (A and B) generated by language models.

Keep in mind that the presented paragraph may be an excerpt from a longer \
document, so it may not be fully self-contained. Do not deduct points for \
issues arising from this.

Text A:
{text_a}

Text B:
{text_b}

Rate each text independently on a scale of 1-10 for each criterion:
1. Clarity and coherence: How clear and understandable is the text?
2. Grammaticality: Are there any grammatical errors in the text?
3. Factuality: If applicable, is the factually verifiable information stated \
in the text (e.g. facts about geography, history, etc.) accurate and \
reliable? If not applicable, score 5 and note N/A.
4. Writing style: How well is the text written in terms of style and fluency? \
Do the sentences flow well, is the vocabulary appropriate?
5. Creativity: How original and creative is the text?

Then state which text is better per criterion (A / B / tie) and give an \
overall winner (A / B / tie) with a one-sentence reason.

Respond ONLY in valid JSON with exactly this structure:
{{
  "A": {{"clarity": 0, "grammaticality": 0, "factuality": 0, "style": 0, "creativity": 0}},
  "B": {{"clarity": 0, "grammaticality": 0, "factuality": 0, "style": 0, "creativity": 0}},
  "pairwise": {{"clarity": "A/B/tie", "grammaticality": "A/B/tie", "factuality": "A/B/tie", "style": "A/B/tie", "creativity": "A/B/tie"}},
  "overall": "A/B/tie",
  "reason": "one sentence"
}}"""


