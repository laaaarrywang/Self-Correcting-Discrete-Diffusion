# Merge shards
# ═══════════════════════════════════════════════════════════════════════════════

def run_merge(args):
    world_size = args.world_size
    work_dir   = Path(args.work_dir)
    out_dir    = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    def _merge_json_shards(paths, out_path, sort_key=None):
        merged = []
        for p in paths:
            if p.exists():
                with open(p) as f:
                    merged.extend(json.load(f))
        if not merged:
            return False
        if sort_key:
            merged.sort(key=sort_key)
        with open(out_path, "w") as f:
            json.dump(merged, f, indent=2)
        print(f"  Merged {len(merged)} items -> {out_path}")
        return True

    for model in ("scdd", "gidd"):
        for total_steps in TOTAL_STEPS_LIST:
            shards = [_shard_path(
                work_dir / f"{model}_texts_steps{total_steps}.json", r, world_size)
                for r in range(world_size)]
            _merge_json_shards(
                shards, work_dir / f"{model}_texts_steps{total_steps}.json")

    all_results = {}
    for total_steps in TOTAL_STEPS_LIST:
        shards = [_shard_path(
            out_dir / f"results_steps{total_steps}.json", r, world_size)
            for r in range(world_size)]
        out_path = out_dir / f"results_steps{total_steps}.json"
        ok = _merge_json_shards(shards, out_path, sort_key=lambda x: x["idx"])
        if ok:
            with open(out_path) as f:
                all_results[total_steps] = json.load(f)
            n_got = len(all_results[total_steps])
            if n_got < NUM_TEXTS:
                print(f"  WARNING: only {n_got}/{NUM_TEXTS} samples — "
                      f"skipping stats for steps={total_steps}")
                _print_summary(all_results[total_steps], total_steps, stats_test=False)
            else:
                _print_summary(all_results[total_steps], total_steps, stats_test=True)

    if all_results:
        with open(out_dir / "all_results.json", "w") as f:
            json.dump(all_results, f, indent=2)
        print(f"\nAll results saved to {out_dir}")


